package odev2;

public class Course {
	private String courseName;
	private int time;
	private int srate;
	private int vrate;
	
	
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public int getSrate() {
		return srate;
	}
	public void setSrate(int rate) {
		this.srate = rate;
	}
	public int getVrate() {
		return vrate;
	}
	public void setVrate(int vrate) {
		this.vrate = vrate;
	}
	
	
	
	
}
